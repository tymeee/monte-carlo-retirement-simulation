# 3. STREAMLIT UI LAYOUT
# ==========================================
st.set_page_config(page_title="Production Portfolio Engine", layout="wide")
st.title("Monte Carlo Asset Allocation Simulator")

with st.spinner("Streaming integrated market histories..."):
    mean_returns, bull_cov, bear_cov = fetch_and_prep_data()

st.sidebar.header("Capital Controls")
initial_amount = st.sidebar.number_input("Principal Amount (THB)", value=3000000, step=100000)
inserted_funds = st.sidebar.number_input("Monthly Contribution", value=10000, step=1000)
withdrawal_pct = st.sidebar.number_input("Target Annual Withdrawal Rate (%)", value=4.0, step=0.1) / 100
withdrawal_amount = (initial_amount * withdrawal_pct) / 12

st.sidebar.header("Asset Distributions")
savings_weight = st.sidebar.slider("Risk-Free Savings Weight", 0.0, 1.0, 0.1)
us_weight = st.sidebar.slider("S&P 500 Index Weight", 0.0, 1.0, 0.1)
thai_weight = st.sidebar.slider("SET Index Weight", 0.0, 1.0, 0.2)
china_weight = st.sidebar.slider("SSE Composite Weight", 0.0, 1.0, 0.1)
bond_mut = st.sidebar.slider("AGG Bond ETF Weight", 0.0, 1.0, 0.1)
agg_mut = st.sidebar.slider("AOA Aggressive Target Allocation", 0.0, 1.0, 0.1)
mod_mut = st.sidebar.slider("AOR Moderate Target Allocation", 0.0, 1.0, 0.1)
con_mut = st.sidebar.slider("AOK Conservative Target Allocation", 0.0, 1.0, 0.1)

total_weight = sum([savings_weight, us_weight, thai_weight, china_weight, bond_mut, agg_mut, mod_mut, con_mut])
pure_cash_weight = 1.0 - total_weight

if total_weight > 1.0:
    st.sidebar.error(f"Asset limits exceeded! Net Allocation: {total_weight:.2f}")
    st.stop()

if st.button("Execute Tensor Simulation"):
    duration = 7560
    num_sims = 1000

    with st.spinner("Constructing cross-border covariance paths..."):
        bull_chol = np.linalg.cholesky(bull_cov)
        bear_chol = np.linalg.cholesky(bear_cov)

        rand_normals = np.random.standard_normal((duration, num_sims, len(mean_returns)))

        bull_returns = rand_normals @ bull_chol.T + mean_returns
        bear_returns = rand_normals @ bear_chol.T + mean_returns

        # Expense ratios realigned strictly to match alphabetical assets
        # China, AGG, AOA, AOK, AOR, US, Thai
        expense_ratios = np.array([0.005, 0.001, 0.003, 0.003, 0.003, 0.001, 0.002])
        daily_fee_drag = (1 - expense_ratios) ** (1 / 252)

        bull_multipliers = np.exp(bull_returns) * daily_fee_drag
        bear_multipliers = np.exp(bear_returns) * daily_fee_drag

    with st.spinner("Processing Monte Carlo matrices..."):
        saving_daily_return = (1 + 0.025) ** (1 / 252)

        sim_results, trials_failed = run_fast_simulations(
            num_sims, duration, initial_amount,
            china_weight, bond_mut, agg_mut, con_mut, mod_mut, us_weight, thai_weight,
            savings_weight, pure_cash_weight, saving_daily_return,
            bull_multipliers, bear_multipliers, withdrawal_amount, inserted_funds
        )

    st.subheader("Performance Metrics")
    col1, col2, col3 = st.columns(3)
    ending_values = sim_results[-1, :]

    col1.metric("Median Portfolio Yield", f"THB {np.percentile(ending_values, 50):,.2f}")
    col2.metric("Portfolio Insolvency Rate", f"{(trials_failed/num_sims)*100:.1f}%")

    avg_inflation_rate = 0.0177
    adj_median = np.percentile(ending_values, 50) / ((1 + avg_inflation_rate) ** (duration / 252))
    col3.metric("Real Purchasing Power Value", f"THB {adj_median:,.2f}")

    fig, ax = plt.subplots(figsize=(10, 4.5))
    percentiles = np.percentile(sim_results, [10, 25, 50, 75, 90], axis=1)

    ax.plot(percentiles[0], label='10th Percentile Risk Bounds', color='red', linestyle='--')
    ax.plot(percentiles[1], label='25th Percentile Downside', color='orange', linestyle=':')
    ax.plot(percentiles[2], label='50th Percentile Median Path', color='black', linewidth=2)
    ax.plot(percentiles[3], label='75th Percentile Upside', color='green', linestyle=':')

    ax.set_title('Decile-Bounded Structural Portfolio Projections')
    ax.set_xlabel('Trading Horizon (Days)')
    ax.set_ylabel('Total Portfolio Capitalization (THB)')
    ax.legend(loc='upper left')
    ax.grid(True, alpha=0.25)

    st.pyplot(fig)