import streamlit as st
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import yfinance as yf
from numba import njit

# ==========================================
# 1. CACHED DATA FETCHING (HOLIDAY RESILIENT)
# ==========================================
@st.cache_data(ttl=86400)
def fetch_and_prep_data(start_date='2000-01-01', end_date='2025-12-31'):
    # yfinance outputs multiple tickers in strict alphabetical order
    tickers = ['000001.SS', 'AGG', 'AOA', 'AOK', 'AOR', '^GSPC', '^SET.BK']
    data = yf.download(tickers, start=start_date, end=end_date)['Close']

    # FIX: Forward fill cross-border holiday gaps so datasets align without dropping rows
    data = data.ffill().bfill()
    data_change = np.log(data / data.shift(1)).dropna()

    bear_data = data_change.loc['2007-01-01':'2010-01-01']
    bull_data = data_change.loc['2011-01-01':'2019-01-01']

    bfr_mean_returns = data_change.mean().values
    variances = np.diag(data_change.cov().values)
    mean_returns = bfr_mean_returns - (variances / 2)

    bull_cov = bull_data.cov().values
    bear_cov = bear_data.cov().values

    return mean_returns, bull_cov, bear_cov

# ==========================================
# 2. ALIGNED NUMBA SIMULATION ENGINE
# ==========================================
@njit
def run_fast_simulations(num_simulations, duration, initial_amount,
                         china_w, bond_w, agg_w, con_w, mod_w, us_w, thai_w,
                         savings_weight, pure_cash_weight, saving_daily_return,
                         bull_multipliers, bear_multipliers, withdrawal_amount, inserted_funds):

    portfolio_simulations = np.zeros((duration, num_simulations))
    trials_failed = 0

    for sim in range(num_simulations):
        # Initial balances mapping strictly to alphabetical assets
        china_stocks = initial_amount * china_w
        bond_mutual = initial_amount * bond_w
        agg_mutual = initial_amount * agg_w
        con_mutual = initial_amount * con_w
        mod_mutual = initial_amount * mod_w
        us_stocks = initial_amount * us_w
        thai_stocks = initial_amount * thai_w
        savings = initial_amount * savings_weight
        pure_cash = initial_amount * pure_cash_weight

        is_bear = np.random.rand() < 0.5

        for day in range(duration):
            if is_bear:
                mults = bear_multipliers[day, sim]
                if np.random.rand() < 0.1: is_bear = False
            else:
                mults = bull_multipliers[day, sim]
                if np.random.rand() < 0.05: is_bear = True

            # Apply returns directly to matching matrix columns
            china_stocks *= mults[0]
            bond_mutual *= mults[1]
            agg_mutual *= mults[2]
            con_mutual *= mults[3]
            mod_mutual *= mults[4]
            us_stocks *= mults[5]
            thai_stocks *= mults[6]
            savings *= saving_daily_return

            if day > 0 and day % 21 == 0:
                china_stocks += inserted_funds * china_w
                bond_mutual += inserted_funds * bond_w
                agg_mutual += inserted_funds * agg_w
                con_mutual += inserted_funds * con_w
                mod_mutual += inserted_funds * mod_w
                us_stocks += inserted_funds * us_w
                thai_stocks += inserted_funds * thai_w
                savings += inserted_funds * savings_weight

                if day > 2520:
                    if pure_cash >= withdrawal_amount: pure_cash -= withdrawal_amount
                    elif savings >= withdrawal_amount: savings -= withdrawal_amount
                    elif agg_mutual >= withdrawal_amount: agg_mutual -= withdrawal_amount
                    elif mod_mutual >= withdrawal_amount: mod_mutual -= withdrawal_amount
                    elif con_mutual >= withdrawal_amount: con_mutual -= withdrawal_amount
                    elif bond_mutual >= withdrawal_amount: bond_mutual -= withdrawal_amount
                    elif us_stocks >= withdrawal_amount: us_stocks -= withdrawal_amount
                    elif china_stocks >= withdrawal_amount: china_stocks -= withdrawal_amount
                    elif thai_stocks >= withdrawal_amount: thai_stocks -= withdrawal_amount
                    else:
                        trials_failed += 1
                        break

            portfolio_simulations[day, sim] = (china_stocks + bond_mutual + agg_mutual +
                                               con_mutual + mod_mutual + us_stocks +
                                               thai_stocks + savings + pure_cash)

    return portfolio_simulations, trials_failed

