import streamlit as st
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import yfinance as yf
from numba import njit

# ==========================================
# 1. CACHED DATA FETCHING (PREVENTS API BANS)
# ==========================================
@st.cache_data(ttl=86400) # Cache for 24 hours
def fetch_and_prep_data(start_date='2000-01-01', end_date='2025-12-31'):
    tickers = ['^GSPC', '^SET.BK', '000001.SS', 'AGG', 'AOA', 'AOR', 'AOK']

    # Download all at once
    data = yf.download(tickers, start=start_date, end=end_date)['Close']
    data_change = np.log(data / data.shift(1)).dropna()

    # Isolate Bear/Bull periods for covariance matrices
    bear_data = data_change.loc['2007-01-01':'2010-01-01']
    bull_data = data_change.loc['2011-01-01':'2019-01-01']

    bfr_mean_returns = data_change.mean().values
    variances = np.diag(data_change.cov().values)
    mean_returns = bfr_mean_returns - (variances / 2) # Geometric adjust

    bull_cov = bull_data.cov().values
    bear_cov = bear_data.cov().values

    return mean_returns, bull_cov, bear_cov

# ==========================================
# 2. NUMBA JIT-COMPILED SIMULATION ENGINE
# ==========================================
# We compile the brutal nested loop logic to C-level speed.
# Pure Python takes minutes. Numba takes milliseconds.
@njit
def run_fast_simulations(num_simulations, duration, initial_amount, stock_alloc, fund_alloc,
                         savings_weight, pure_cash_weight, saving_daily_return,
                         bull_multipliers, bear_multipliers, withdrawal_amount, inserted_funds):

    portfolio_simulations = np.zeros((duration, num_simulations))
    trials_failed = 0

    # Flatten allocations for easier indexing
    us_weight, thai_weight, china_weight = stock_alloc
    bond_mut, agg_mut, mod_mut, con_mut = fund_alloc

    for sim in range(num_simulations):
        # Initial Balances
        us_stocks = initial_amount * us_weight
        thai_stocks = initial_amount * thai_weight
        china_stocks = initial_amount * china_weight
        bond_mutual = initial_amount * bond_mut
        agg_mutual = initial_amount * agg_mut
        mod_mutual = initial_amount * mod_mut
        con_mutual = initial_amount * con_mut
        savings = initial_amount * savings_weight
        pure_cash = initial_amount * pure_cash_weight

        is_bear = np.random.rand() < 0.5

        for day in range(duration):
            # Market Regime Switching
            if is_bear:
                mults = bear_multipliers[day, sim]
                if np.random.rand() < 0.1: is_bear = False
            else:
                mults = bull_multipliers[day, sim]
                if np.random.rand() < 0.05: is_bear = True

            # Apply Returns
            us_stocks *= mults[0]
            thai_stocks *= mults[1]
            china_stocks *= mults[2]
            bond_mutual *= mults[3]
            agg_mutual *= mults[4]
            mod_mutual *= mults[5]
            con_mutual *= mults[6]
            savings *= saving_daily_return

            # Monthly Injections
            if day > 0 and day % 21 == 0:
                us_stocks += inserted_funds * us_weight
                thai_stocks += inserted_funds * thai_weight
                china_stocks += inserted_funds * china_weight
                bond_mutual += inserted_funds * bond_mut
                agg_mutual += inserted_funds * agg_mut
                mod_mutual += inserted_funds * mod_mut
                con_mutual += inserted_funds * con_mut
                savings += inserted_funds * savings_weight

                # Withdrawals (The sequential drain logic you requested)
                # Draining in order: Cash -> Savings -> Aggressive -> Mod -> Con -> Bond -> Equity
                if day > 2520: # Assuming withdrawals start after phase 1
                    if pure_cash >= withdrawal_amount: pure_cash -= withdrawal_amount
                    elif savings >= withdrawal_amount: savings -= withdrawal_amount
                    elif agg_mutual >= withdrawal_amount: agg_mutual -= withdrawal_amount
                    elif mod_mutual >= withdrawal_amount: mod_mutual -= withdrawal_amount
                    elif con_mutual >= withdrawal_amount: con_mutual -= withdrawal_amount
                    elif bond_mutual >= withdrawal_amount: bond_mutual -= withdrawal_amount
                    elif us_stocks >= withdrawal_amount: us_stocks -= withdrawal_amount
                    else:
                        trials_failed += 1
                        break # Bankrupt

            portfolio_simulations[day, sim] = (us_stocks + thai_stocks + china_stocks +
                                               bond_mutual + agg_mutual + mod_mutual +
                                               con_mutual + savings + pure_cash)

    return portfolio_simulations, trials_failed

