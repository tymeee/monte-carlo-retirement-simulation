@st.cache_data
def build_market_stats():

    combined_data = load_market_data()

    bear_data = combined_data.loc[
        "2005-01-01":"2009-04-01"
    ]

    bull_data = combined_data.loc[
        "2010-03-01":"2019-01-01"
    ]

    bull_mean = bull_data.mean()
    bear_mean = bear_data.mean()

    bull_cov = bull_data.cov()
    bear_cov = bear_data.cov()

    bull_var = np.diag(bull_cov)
    bear_var = np.diag(bear_cov)

    bull_adj = bull_mean - bull_var / 2
    bear_adj = bear_mean - bear_var / 2

    return (
        bull_adj.values,
        bear_adj.values,
        bull_cov.values,
        bear_cov.values
    )

